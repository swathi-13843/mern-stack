const day = "wednesday";
switch (day) {
    case "sunday":
        console.log("Rest day");
        break;
    case "monday":
        console.log("Holiday");
        break;
    case "tuesday":
        console.log("Prepare for class");
        break;
    case "wednesday":
        console.log("Conduct class");
        break;
    case "thursday":
        console.log("Review student work");
        break;
    case "friday":
        console.log("Plan next week");
        break;
    default:
        console.log("Invalid day");
    }